def van_eck_number(n):

    sequence = [0]

    while len(sequence) < n:

        previous = sequence[-1]

        if previous not in sequence[:-1]:

            sequence.append(0)

        else:

            last_index = len(sequence) - 1

            previous_index = (
                len(sequence)
                - 1
                - sequence[-2::-1].index(previous)
            )

            sequence.append(
                last_index - previous_index
            )

    return sequence[n - 1]


def generate(length):

    if length <= 0:

        return []

    sequence = [0]

    while len(sequence) < length:

        previous = sequence[-1]

        positions = [
            i
            for i, value in enumerate(sequence[:-1])
            if value == previous
        ]

        if not positions:

            sequence.append(0)

        else:

            sequence.append(
                (len(sequence) - 1) - positions[-1]
            )

    return sequence


def recognize(sequence):

    generated = generate(len(sequence))

    return generated == sequence


def fit(sequence):

    if recognize(sequence) is not True:

        return None

    return {}


def evaluate(parameters, n):

    return generate(n)[-1]


def formula(parameters):

    return format_formula(
        "a(n)=distance since previous occurrence"
    )


def complexity(_):

    return 3


