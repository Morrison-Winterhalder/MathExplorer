def motzkin_number(n):

    if n == 1:
        return 1

    if n == 2:
        return 1

    previous_two = 1
    previous_one = 1

    for k in range(3, n + 1):

        current = (
            ((2 * k - 1) * previous_one)
            +
            ((3 * k - 6) * previous_two)
        ) // (k + 1)

        previous_two = previous_one
        previous_one = current

    return previous_one


