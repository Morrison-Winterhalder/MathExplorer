def is_happy(n):

    seen = set()

    while n != 1:

        if n in seen:
            return False

        seen.add(n)

        n = sum(
            int(digit) ** 2
            for digit in str(n)
        )

    return True


def recognize(sequence):

    expected = [
        n
        for n in range(1, 1000)
        if is_happy(n)
    ][:len(sequence)]

    return sequence == expected


def fit(sequence):

    if recognize(sequence):
        return {}

    return None


def evaluate(parameters, n):

    return [
        x
        for x in range(1, 1000)
        if is_happy(x)
    ][n - 1]


