def stopping_time(n):

    steps = 0

    while n != 1:

        if n % 2 == 0:
            n //= 2

        else:
            n = 3 * n + 1

        steps += 1

    return steps


def recognize(sequence):

    expected = [
        stopping_time(n)
        for n in range(1, len(sequence) + 1)
    ]

    return sequence == expected


